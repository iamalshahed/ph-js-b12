/**
 * === first check type. if type is different, they are different.
 * if type is same, then checks the value. if value is same --->> true or if value is different --->> false
 */

console.log(2 === 2);
console.log(2 === "2");
console.log(1 === 2);
console.log(true === 1);
console.log(false === 0);
console.log(null === undefined);
console.log(0 === "");
